  <div class="page-header">
    <h1>About the game</h1>
  </div>
  <p>Catacomb Snatch is a 2D top-down shooter/RTS originally created by Mojang for the Humble Bundle charity event and was released open-source into the wild. This site represents the open-source Maescool repository modification of this game.</p>
  <h2>Basic gameplay</h2>
  <p>Your job is to collect 50 batches of treasure from the Treasure Trove located somewhere on the map before your opponent.</p>
  <ul>
    <li>Gather money by destroying enemies and collecting the coins that drop out</li>
    <li>Buy automatic turrets, harvesters and bombs from your Base Camp to help you defend and expand your territory, earn money and destroy obstacles</li>
    <li>Build a railway to the Treasure Trove and create Carts to gather batches 50 Treasure</li>
  </ul>
  <h2>Controls</h2>
  <p>Move: Arrows / WASD<br>
    Shoot(lock firing angle): Space / C / Shift / Alt / Ctrl<br>
    Buy/Pick Up: E / Z<br>
    Lay/Remove Track: R / X (10/15 Coins to add/remove railway sections)<br>
    Create a Cart: R / X while standing on the first rail in your base (50 coins)</p>
  <h2>Health</h2>
  <p>The player starts with 10/10 HP, which regenerates over time if you take damage.</p>
  <h2>Items</h2>
  <p>When enemies or Tombs are destroyed, coins appear. Grab them before they disappear so you can buy the following items at your Base Camp! (use E or Z while standing in front of an item)</p>
  <ul>
    <li>Turret (150 coins): Shoots baddies in general vicinity once placed. Can be picked up and moved.</li>
    <li>Collector (Aka Harvester) (300 coins): Sucks up coins within a large radius. Pick up the collector to receive the coins. They can also be destroyed by enemies, so protect them!</li>
    <li>Bomb (500 coins): Used to destroy purple gemmed walls. Shoot the bomb or have turrets shoot it to detonate it once placed.</li>
    <li>Cart (Aka raildroid) (50 coins): Rail carts can be bought by standing on the first section of track at your Base Camp. These will travel along rails that you lay down and will collect treasure from the Treasure Trove at the center of the map. You can also place Turrets and Collectors on the Carts.</li>
  </ul>
  <h2>Enemies</h2>
  <p>Bats, Cobras, Mummies (Take a lot of damage, drop a lot of coins!), Tombs (These spawn enemies), Pharaohs and also the other player!</p>
  <h2>Multiplayer</h2>
  <p>Catacomb Snatch can be played with your Friends! One of you will Host the game while the other will join. If you're hosting behind a firewall/router, you'll have to forward Port 3000 to your computer's IP If you don't have access to your firewall/router, you might consider using Hamachi</p>
